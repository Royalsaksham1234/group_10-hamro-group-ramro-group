public class TypeCasting2 {
    public static void main(String[]args) {
        int a = 7;
        int b = 9;
        System.out.println(a++);
        System.out.println(b++);
        System.out.println(a + ++b);
        System.out.println(++a + b-- + ++a + a + b--);
        System.out.println(++a + ++b + ++a + a);
        System.out.println(a );
        System.out.println(b);

    }
    
}
